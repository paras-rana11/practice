// JavaScript Logical Operators
// Operator	        Description
// &&	            logical and
// ||	            logical or
// !	            logical not

// JavaScript Logical Operators

// &&  - Logical AND: Returns true if both operands are true
let t = true;
let f = false;
console.log("t && f:", t && f); // Output: false (because b is false)

// ||  - Logical OR: Returns true if at least one of the operands is true
console.log("t || f:", t || b); // Output: true (because a is true)

// !   - Logical NOT: Reverses the logical state of its operand
console.log("!t:", !t); // Output: false (because a is true and we negate it)

