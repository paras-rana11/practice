// JavaScript Assignment Operators:
// Assignment operators assign values to JavaScript variables.

// Operator         Example          Same As
// =                x = y            x = y
// +=               x += y           x = x + y
// -=               x -= y           x = x - y
// *=               x *= y           x = x * y
// /=               x /= y           x = x / y
// %=               x %= y           x = x % y
// **=              x **= y          x = x ** y

// = (Simple Assignment)
let x = 10;
console.log("x = 10: " + x);  // Initial value of x
x = 15;  // Assign 15 to x
console.log("x = 15: " + x);  // x is now 15

// += (Addition Assignment)
x += 5;  // Same as x = x + 5
console.log("x += 5: " + x);  // 15 + 5 = 20

// -= (Subtraction Assignment)
x -= 3;  // Same as x = x - 3
console.log("x -= 3: " + x);  // 20 - 3 = 17

// *= (Multiplication Assignment)
x *= 2;  // Same as x = x * 2
console.log("x *= 2: " + x);  // 17 * 2 = 34

// /= (Division Assignment)
x /= 4;  // Same as x = x / 4
console.log("x /= 4: " + x);  // 34 / 4 = 8.5

// %= (Modulus Assignment)
x %= 3;  // Same as x = x % 3
console.log("x %= 3: " + x);  // 8.5 % 3 = 2.5

// **= (Exponentiation Assignment)
x **= 2;  // Same as x = x ** 2
console.log("x **= 2: " + x);  // 2.5 ** 2 = 6.25

